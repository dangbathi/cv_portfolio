window.addEventListener('keyup', function(event){
    let key = event.key;
    switch(key){
        case 1:
            alert('bạn bấm phím 1');
            break;
        case 2:
            alert('bạn bấm phím 2');
            break;
        case 3:
            alert('bạn bấm phím 3');
            break;
        case 4:
            alert('bạn bấm phím 4');
            break;
        case 5:
            alert('bạn bấm phím 5');
            break;
        case 6:
            alert('bạn bấm phím 6');
            break;
        case 7:
            alert('bạn bấm phím 7');
            break;
        case 8:
            alert('bạn bấm phím 8');
            break;
        case 9:
            alert('bạn bấm phím 9');
            break;
        case 0:
            alert('bạn bấm phím 0');
            break;
        default :
            document.write("Màu bạn nhập không có trong hệ thống");
    }
})